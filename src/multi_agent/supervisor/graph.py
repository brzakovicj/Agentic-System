from langchain_core.messages import SystemMessage, HumanMessage, AIMessage
from langgraph.graph import StateGraph, END
from langgraph.prebuilt import ToolNode
from langgraph.checkpoint.memory import MemorySaver
from datetime import datetime
from dotenv import load_dotenv
from langgraph.types import RunnableConfig
from langchain_ollama.chat_models import ChatOllama
from src.multi_agent.copywriter.graph import CopywriterAgent
from src.multi_agent.researcher.graph import ResearcherAgent
from src.multi_agent.supervisor.state import SupervisorState
from src.multi_agent.supervisor.tools import handoff_to_subagent

load_dotenv()

class SupervisorAgent:
    def __init__(self):
        self.graph = None
        self.llm = None
        self.tools = None
        self.supervisor_prompt = open("src/prompts/supervisor.md", "r").read()

        self.tools = [handoff_to_subagent]

        # LLM
        self.llm = ChatOllama(
            model="llama3.2:3b",
            temperature=0,
        )

        self.llm_with_tools = self.llm.bind_tools(self.tools)

        self.research_agent = ResearcherAgent()
        self.copywriter_agent = CopywriterAgent()

    async def build_graph(self):
        """Kreira LangGraph."""
        builder = StateGraph(SupervisorState)

        self.research_graph = await self.research_agent.initialize()
        self.copywriter_graph = await self.copywriter_agent.initialize()

        builder.add_node(
            self.supervisor, 
        # This is a new feature and is only needed for visualization, doesn't impact runtime
        # destinations=("tools", "call_researcher", "call_copywriter", END)
        )
        builder.add_node("tools", ToolNode(self.tools))
        builder.add_node(self.call_researcher)
        builder.add_node(self.call_copywriter)

        builder.set_entry_point("supervisor")

        builder.add_conditional_edges(
            "supervisor",
            self.supervisor_router,
            {
                "tools": "tools",
                END: END,
            }
        )
        # Normally we route back to the agent after a tool call. In this case we're using the Command primitive in the tool call to do the routing. So we don't need this typical edge.
        # builder.add_edge("tools", "supervisor")

        # Now every time we call a sub-agent we need to route back to the supervisor
        builder.add_edge("call_researcher", "supervisor")
        builder.add_edge("call_copywriter", "supervisor")

        self.graph = builder.compile(checkpointer=MemorySaver())

        return self.graph
    
    async def supervisor(self, state: SupervisorState):
        """The main supervisor agent."""

        print("\n--- SUPERVISOR STATE MESSAGES ---")
        for msg in state.messages:
            print(msg)
        print("--------------------------------\n")

        response = self.llm_with_tools.invoke([
            SystemMessage(content = self.supervisor_prompt.format(current_datetime=datetime.now()))
            ] + state.messages)
        return {"messages": [response]}

    async def supervisor_router(self, state: SupervisorState) -> str:
        """Route to the tools node if the supervisor makes a tool call."""
        if state.messages[-1].tool_calls:
            return "tools"
        return END
    
    async def call_researcher(self, state: SupervisorState, config: RunnableConfig):
        """Call the researcher agent.
        
        The agent is invoked only with the task description generated by the supervisor, so the context window is not cluttered with the full conversation history of the supervisor.
        """
        research_response = await self.research_graph.ainvoke(
            input={
                "messages": [HumanMessage(content=state.task_description)],
                },
            config=config,
        )

        ai_message = AIMessage(name="researcher", content=research_response["messages"][-1].content)

        return {
            "research_reports": research_response["research_reports"],
            "messages": [ai_message],
        }

    async def call_copywriter(self, state: SupervisorState, config: RunnableConfig):
        """Call the copywriter agent.
        
        The agent is invoked only with the task description generated by the supervisor, and any research reports that have been generated by the researcher.
        """
        copywriter_response = await self.copywriter_graph.ainvoke(
            input={
                "messages": [HumanMessage(content=state.task_description)],
                "research_reports": state.research_reports,
                },
            config=config,
        )
        
        ai_message = AIMessage(name="copywriter", content=copywriter_response["messages"][-1].content)

        return {"messages": [ai_message]}

